function getRandomHadith() {
    const randomIndex = Math.floor(Math.random() * hadiths.length);
    return hadiths[randomIndex];
}

async function execute({ sock, msg, args }) {
    const chatId = msg.key.remoteJid;
    const sender = decode(msg.key.participant || chatId);
    const senderNumber = sender.split('@')[0];

    try {
        // تفاعل بداية
        await sock.sendMessage(chatId, {
            react: { text: "📖", key: msg.key }
        });

        // اختيار حديث عشوائي
        const hadith = getRandomHadith();
        
        // بناء نص الحديث
        const hadithText = `*‏❖══════ •『📖』• ══════❖*

*❍↵ حــــديــــث نــــبــــوي*

*❃━═━═✦•〘•🕋•〙•✦═━═━❃*

*╮──〔 الــــمــــتــــن 〕──╭*

┊ ⌁ ${hadith.text}

*❃━═━═✦•〘•📜•〙•✦═━═━❃*

*╮──〔 الــــمــــصــــدر 〕──╭*

┊ ⌁ 📚 ${hadith.book}
┊ ⌁ رقم الحديث: ${hadith.number}
┊ ⌁ الراوي: ${hadith.narrator}

*‏❖══════ •『 🪻 』• ══════❖*

> ⏤͟͟͞͞ 𝐊𝐑𝐎𝐋𝐋𝐎 - 𝑩𝛩𝑻 🕸⃝⃕

👤 *الطلب من:* @${senderNumber}`;

        // إرسال الحديث
        await sock.sendMessage(chatId, {
            text: hadithText,
            mentions: [sender]
        }, { quoted: msg });

        // تفاعل نجاح
        await sock.sendMessage(chatId, {
            react: { text: "✅", key: msg.key }
        });

        console.log(`📖 حديث نبوي لـ @${senderNumber}`);

    } catch (err) {
        console.error("❌ خطأ في جلب الحديث:", err);
        await sock.sendMessage(chatId, { 
            text: "❌ حدث خطأ في جلب الحديث، حاول مرة أخرى لاحقاً" 
        });
        
        await sock.sendMessage(chatId, {
            react: { text: "❌", key: msg.key }
        });
    }
}

export default { NovaUltra, execute };